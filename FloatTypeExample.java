class FloatTypeExample{
	public static void main(String args[])
	{
		float salary;
		float version;
		float percentage;
		float dataspeed;
		float distance;
		
		salary = 3.8f;
		version = 1.8f;
		percentage = 82.30f;
		dataspeed = 12.3f;
		distance = 28.6f;
		
		System.out.println("salary of a employee per annum "+salary);
		System.out.println("java version "+version);
		System.out.println("persentage obtained in MCA "+percentage);
		System.out.println("speed of data is "+dataspeed+"kbps");
		System.out.println("distance from bhadravathi to shimogga is "+distance+"km");
	}
}